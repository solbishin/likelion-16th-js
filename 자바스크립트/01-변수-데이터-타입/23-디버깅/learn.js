// 3. "분명 맞게 짰는데 왜 안 되지?"라며 운을 탓한다.


// 설명:
// 훌륭한 개발자가 되기 위해 필요한 핵심 역량은 '완벽한 코드 작성'이 아닙니다.
// 오히려 자신의 실수를 빠르게 인정하고, 브라우저가 주는 메시지(단서)를 바탕으로
// 문제를 해결해 나가는 '태도'와 '기술'이 실력을 결정짓습니다.


// --------------------------------------------------------------------------
// 핵심 요약!
