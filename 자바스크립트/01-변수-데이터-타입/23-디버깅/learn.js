// 2. 디버깅은 단순히 버그를 잡는 것이 아니라 논리적 사고를 훈련하는 기회입니다.
// 3. 오류 메시지는 여러분을 공격하는 도구가 아니라 성장을 돕는 나침반입니다.
// 4. "왜 안 되지?"라는 의문보다 "어떻게 작동하고 있지?"라는 분석이 먼저입니다.
