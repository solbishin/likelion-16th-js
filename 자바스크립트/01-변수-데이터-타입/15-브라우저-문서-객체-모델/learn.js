// 모델링(Modeling)이란?
// - 실물을 조작하기 좋게 만든 모형
// - 실제 사물의 특징과 구조를 뽑아, 컴퓨터가 조작하기 쉬운 형태(객체)로 만드는 것
//   * HTML → 건물
//   * DOM  → 건물 관리 모형
// - 모델링된 DOM을 JavaScript로 조작하면 실제로 변경
//   * 예) 모형의 101호 버튼을 클릭해 '전등 끄기'를 실행하면
//         실제 101호 건물의 전등이 꺼집니다.

// 모델링을 하는 이유
// - HTML 소스 코드는 단순한 텍스트
// - JavaScript는 단순한 텍스트를 직접 조작할 수 없음
// - 브라우저가 HTML 구조를 본떠 모형(객체) 생성
//   * 텍스트
//     # <button type="button">자세히 보기</button>
//   * 객체
//     # 이름 : button
//     # 속성 : type="button"
//     # 내용 : '자세히 보기'
//     # 기능 : 클릭하면 알림창 띄우기


